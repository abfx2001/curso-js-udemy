// COMANDOS DO NPM
// npm init -y  -> gera o arquivo de inicialização do projeto e dependências.
// npm i (dependência) -> instala a dependência na última versão.
// npm update -> faz o update das dependências.
// npm i (dependência) --save-dev -> instala a dependência na parte de desenvolvimento.
// npm i (dependência) --save-prod -> instala a dependência na parte de produção(padrão).
// npm i (dependência)@1.2.3 -> faz a instalação da versão em escolhida.
// npm uninstall (dependência) -> desinstala a dependência.
// npm ls -> lista as dependências completas.
// npm ls --depth=n (n = número de pastas que vai ser aberto) -> lista quantas pastas vai ser mostrada.
// npm outdated -> mostra o que pode ser atualizado.

